package com.core.reflection.access;

public class User {
    private Long userId;
    private String username;
    private String password;
    private int age;

    public User() {
    }

    public User(Long userId, String username, String password, int age) {
        this.userId = userId;
        this.username = username;
        this.password = password;
        this.age = age;
    }


}
